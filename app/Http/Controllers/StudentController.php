    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'nama' => 'required|min:3',
            'rombel' => 'required',
            'nisn' => 'required|numeric',
            'rayon' => 'required',
        ]);

        student::create([
            'nama' => $request->nama,
            'rombel' => $request->rombel,
            'nisn' => $request->nisn,
            'rayon' => $request->rayon,
        ]);

        return redirect()->back()->with('sukses','berhasil di tambah!');
    }

